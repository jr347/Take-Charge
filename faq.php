            <p><strong>What is the Take Charge Sales Lead Program?</strong>
            <br>
            Take Charge is one of the primary vehicles driving employee engagement in growth at UPS, giving UPSers the chance to be rewarded for submitting leads that generate profitable volume. Thousands of UPSers from all business units have participated in Take Charge and small package business unit package car driver participation in the U.S. has reached record numbers. The program is continually evaluated to ensure it brings the most value to employees and UPS.</p>

            <p><strong>Who is eligible to participate in Take Charge?</strong>
            <br>
            All UPS employees can participate by submitting quality sales leads here in the app. Drivers may also enter leads through their DIADs. While all UPSers are encouraged to participate, only employees at pay band 30 or below may receive award points for leads that produce volume.</p>

            <p><strong>How am I rewarded for submitting quality leads that produce volume?</strong>
            <br>
            Whether you are union-represented or union-free, you are rewarded for sold leads that produce volume by accruing award points that can be redeemed for merchandise. Award points are based on the level of service and number of packages your sales leads generate. Once a lead has sold, the first full week from the sold date starts a 13-week measurement period. Award points are accumulated during this measurement period by the number and type of packages shipped.</p>

          The award point structure assigns different point values across product lines based on profitability, 5 award points are equivalent to 1 AwardperQ. The following chart provides the current value based on the product/service your leads are producing:</p>

            <img src="table.png" alt="Value Chart" height="280">
            <br>
            <br>

            <p><strong>Will I be taxed on rewards I earn?</strong>
            <br>
            Rewards are considered taxable income and are subject to local, state, and federal taxes.</p>

            <p><strong>Can I share my award points?</strong>
            <br>
            No. Award points are not transferable.</p>

            <p><strong>How do I redeem my award points and check my point balance?</strong>
            <br>
            Award points can be redeemed on the Take Charge Rewards Site website for a wide array of merchandise, gifts, travel options, and gift-cards. You can also find your award points balance on this site. If you have questions, you can call the customer service center at 1-800-870-6927.
            <br>
          <br>
            Award points are issued once results have been finalized each month. Award points reflecting your performance for the previous month should be visible by the first of the next month. Remember, it takes 5 award points to equal 1 AwardperQ.
            <br>
            If you decide to leave UPS, your award points stay active until they have been redeemed.</p>

            <p><strong>Will I be charged for shipping and handling?</strong>
            If redeeming from the award vendor catalog, the shipping/handling charges are covered. There are no additional charges. The shipping/handling cost is factored into the point value of the merchandise being redeemed/ordered.</p>


